    public function addColumns(): PowerGridEloquent
    {
        return PowerGrid::eloquent()
            ->addColumn('id')
            ->addColumn('name')

            /** Example of custom column using a closure **/
            ->addColumn('name_lower', function (Kategori $model) {
                return strtolower(e($model->name));
            })

            ->addColumn('created_at_formatted', fn (Kategori $model) => Carbon::parse($model->created_at)->format('d/m/Y H:i:s'))
            ->addColumn('updated_at_formatted', fn (Kategori $model) => Carbon::parse($model->updated_at)->format('d/m/Y H:i:s'));
    }

